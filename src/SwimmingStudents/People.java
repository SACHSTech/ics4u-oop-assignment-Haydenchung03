package SwimmingStudents;

import SwimmingStudents.*;

public class People {

  private String strFirstName;
  private String strLastName;

  public People(String firstName, String lastName) {
    this.strFirstName = firstName;
    this.strLastName = lastName;
  }

  public getFirstName() {
    return strFirstName;
  }

  public getLastName() {
    return strLastName;
  }
}